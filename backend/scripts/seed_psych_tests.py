# ── Runner ────────────────────────────────────────────────────────────────────

async def seed():
    engine = create_async_engine(settings.DATABASE_URL, echo=False)
    async_session = sessionmaker(engine, class_=AsyncSession, expire_on_commit=False)

    async with async_session() as db:
        # 1. Удалить старые данные (results → questions → tests)
        print("Удаляем старые данные...")
        await db.execute(delete(PsychTestResult))
        await db.execute(delete(PsychTestQuestion))
        await db.execute(delete(PsychTest))
        await db.commit()

        # 2. Вставить новые тесты и вопросы
        for t in TESTS:
            test = PsychTest(name=t["name"], description=t["description"])
            db.add(test)
            await db.flush()

            for pos, (q_text, options) in enumerate(t["questions"]):
                db.add(PsychTestQuestion(
                    test_id=test.id,
                    question=q_text,
                    options=options,
                    position=pos,
                ))

            print(f"  ✓ {t['name']}: {len(t['questions'])} вопросов")

        await db.commit()
        print("Готово! Все тесты успешно добавлены.")

    await engine.dispose()


if __name__ == "__main__":
    asyncio.run(seed())
